def generate_persona_prompts():
    with open(DATA_DIR / "full_personas.json", "r", encoding="utf-8") as f:
        data = json.load(f)

    for persona in data["archetypes"]:
        pid = persona["id"]
        if pid in SYSTEM_PROMPT_TEMPLATES:
            persona["system_prompt"] = SYSTEM_PROMPT_TEMPLATES[pid]
            print(f"✅ {pid}: system_prompt 已添加")
        else:
            # 变体从基础继承或自动生成
            base_id = pid.split("_")[0] if "_" in pid else pid
            if base_id in SYSTEM_PROMPT_TEMPLATES:
                base_prompt = SYSTEM_PROMPT_TEMPLATES[base_id]
                morph_note = f"\n【注意】这是 {base_id} 的变体（{pid}），traits 可能有微调。保持基础人格的核心说话风格，但可以加入变体特有的语气变化。"
                persona["system_prompt"] = base_prompt + morph_note
                print(f"  ↳ {pid}: 继承自 {base_id}")
            else:
                print(f"⚠️ {pid}: 暂无 prompt 模板")

    output_path = DATA_DIR / "full_personas_with_prompts.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\n输出: {output_path}")
    return output_path


